##列表的 下标索引
my_list = [1,'lover',True]  #索引从0开始,超出索引会报错
print(my_list[0]) #从左向右
print(my_list[1])
print(my_list[2])
print("---------------------")
print(my_list[-1]) #从右向左
print(my_list[-2])
print(my_list[-3])
print("---------------------")
my_list2 = [[1,2],['love','gem']]
print(my_list2[0][0])
print(my_list2[1][0])
print(my_list2[1][1 ])
